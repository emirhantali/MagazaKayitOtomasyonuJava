      private boolean kullaniciDogrula(String kullaniciAdi, String sifre) {
        Connection baglanti = null;
        Dbhelper dbhelper = new Dbhelper();
        PreparedStatement statement = null;
        ResultSet resultSet = null;
        boolean dogrulandi = false;

        try {
            baglanti = dbhelper.getConnection();
            String sql = "SELECT KullaniciAdi,Şifre FROM yöneticiler WHERE KullaniciAdi = ? AND Şifre = ?";
            statement = baglanti.prepareStatement(sql);
            statement.setString(1, kullaniciAdi);
            statement.setString(2, sifre);

            resultSet = statement.executeQuery();
            dogrulandi = resultSet.next();  
        } catch (SQLException exception) {
            System.out.println("Hata" + exception.getMessage());
            dbhelper.showErrorMessage(exception);
        } finally {
            try {
                if (resultSet != null) resultSet.close();
                if (statement != null) statement.close();
                if (baglanti != null) baglanti.close();
            } catch (SQLException ex) {
                dbhelper.showErrorMessage(ex);
            }
        }

        return dogrulandi;
    }
    
    
    
    
    
    
    
