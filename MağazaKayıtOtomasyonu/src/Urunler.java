
public class Urunler {
    
    private int  StokNo;
    
    private String ÜrünAdi;
    
    private String Beden;
    
    private String Renk;
    
    private String Cinsiyet;
    
    private int StokAdedi;

    public Urunler(int StokNo, String ÜrünAdi, String Beden, String Renk, String Cinsiyet, int StokAdedi) {
        this.StokNo = StokNo;
        this.ÜrünAdi = ÜrünAdi;
        this.Beden = Beden;
        this.Renk = Renk;
        this.Cinsiyet = Cinsiyet;
        this.StokAdedi = StokAdedi;
    }

    public int getStokNo() {
        return StokNo;
    }

    public void setStokNo(int StokNo) {
        this.StokNo = StokNo;
    }

    public String getÜrünAdi() {
        return ÜrünAdi;
    }

    public void setÜrünAdi(String ÜrünAdi) {
        this.ÜrünAdi = ÜrünAdi;
    }

    public String getBeden() {
        return Beden;
    }

    public void setBeden(String Beden) {
        this.Beden = Beden;
    }

    public String getRenk() {
        return Renk;
    }

    public void setRenk(String Renk) {
        this.Renk = Renk;
    }

    public String getCinsiyet() {
        return Cinsiyet;
    }

    public void setCinsiyet(String Cinsiyet) {
        this.Cinsiyet = Cinsiyet;
    }

    public int getStokAdedi() {
        return StokAdedi;
    }

    public void setStokAdedi(int StokAdedi) {
        this.StokAdedi = StokAdedi;
    }

 
    
    
    
    
    
    
    
    
    
    
    
    
    
}
